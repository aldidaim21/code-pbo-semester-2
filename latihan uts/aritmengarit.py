class aritmatika:

    @staticmethod
    def tambah(a, b):
        return a+b

    @staticmethod
    def kurang(a, b):
        return a-b

    @staticmethod
    def kal(a, b):
        return a*b

    @staticmethod
    def bulat(a, b):
        return a//b

    @staticmethod
    def pangkat(a, b):
        return a**b


# objek
print(aritmatika.tambah(10, 11))
